function add(num1, num2) {
    res = num1 + num2
    console.log("sum of "+num1+" and "+num2+": "+ res)
}

function sub(num1, num2) {
    res1 = num1 - num2
    console.log("Minus of "+num1+" and "+num2+": "+ res1)
}

function mult(num1, num2) {
    res2 = num1 * num2
    console.log("prod of "+num1+" and "+num2+": " + res2)
}

function div(num1, num2) {
    res3 = num1 / num2
    console.log("quotient of "+num1+" and "+num2+": "+ res3)
}



